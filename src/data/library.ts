/** 指定IDのライブラリパックを正規化して返す(ピンイン自動生成込み) */
export function getLibraryPack(id: string): { pack: Pack; items: Item[] } | null {
  const def = defs.find((d) => d.id === id)
  if (!def) return null
  const pack = normalizePack(
    { id: def.id, name: def.name, description: def.description, icon: def.icon, color: def.color },
    def.name
  )
  const items: Item[] = []
  let n = 0
  for (const [t, category, subcategory, zh, ja, difficulty, tags] of def.entries) {
    const item = normalizeItem(
      {
        id: `${def.id}-${String(++n).padStart(3, '0')}`,
        type: TYPE_MAP[t],
        category,
        subcategory,
        zh,
        ja,
        difficulty,
        tags: tags ?? [],
        source: 'ライブラリ',
        author: 'Claude',
      },
      pack.id
    )
    if (item) items.push(item)
  }
  return { pack, items }
}
